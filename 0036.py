def isValidSudoku(board: list[list[str]]) -> bool:
    for i in range (0,9):
        
        hashMap = {}
        for j in range(0,9):
            if board[i][j] == ".":
                continue
            elif hashMap.get(board[i][j],False) == True :
                return False
            else:
               hashMap[board[i][j]] = True 
    return True               
  
  
                  
                  
